    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        List<jogo> lista = new ArrayList<jogo>();
        jogoController controller = new jogoController();
        lista = controller.listarJogo(txtNomePesquisa.getText());

        String[] colunas = {"Nome","Plataforma","Desenvolvedora","Situacao","IdadeRecomendada","Preco"};
        modelo.setColumnIdentifiers(colunas);
        modelo.setNumRows(0);

        for(jogo j: lista){
            modelo.addRow(new Object[]{
                j.getNome(),
                j.getPlataforma(),
                j.getDesenvolvedora(),
                j.getSituacao(),
                j.getIdadeRecomendada(),
                j.getPreco()
            });
        }
        tabelaCliente.setModel(modelo);
    }//GEN-LAST:event_btnBuscarActionPerformed

