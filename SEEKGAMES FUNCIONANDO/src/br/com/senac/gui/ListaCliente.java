    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null,"Deseja excluir esse registro?");
    
        if(resposta==0){
           try{
                int id = Integer.parseInt(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(),0).toString());

                clienteController controller = new clienteController();
                controller.excluirCliente(id);

                btnBuscar.doClick();
                JOptionPane.showMessageDialog(null, "Registro Excluido");
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,"Selecione um registro.");
                }
        }
    }//GEN-LAST:event_jButton3ActionPerformed
void pegadados () {
    DefaultTableModel model=( DefaultTableModel) tabelaCliente.getModel ();
    int linha=tabelaCliente.getSelectedRow ();
    txtNome.setText (model.getValueAt (linha,0) .toString ());
    txtCPF.setText (model.getValueAt (linha,1) .toString ());
    txtTelefone.setText (model.getValueAt (linha,2) .toString ());
    txtRua.setText (model.getValueAt (linha,3) .toString ());
    txtBairro.setText (model.getValueAt (linha,4) .toString ());
    txtNumero.setText (model.getValueAt (linha,5) .toString ());
    txtIdade.setText (model.getValueAt (linha,6) .toString ());
    txtPontos.setText (model.getValueAt (linha,7) .toString ());
} 
